import random

def jogar():
    abertura()
    palavra_secreta = secret_word()
    letras_acertadas = initialize_correct_letters(palavra_secreta)
    print(letras_acertadas)

    enforcou = False
    acertou = False
    tentativas = 0

    while(not enforcou and not acertou):
        chute = pede_chute()

        if(chute in palavra_secreta):
            marcando_letras(chute, letras_acertadas, palavra_secreta)
        else:
            tentativas += 1
            desenha_forca(tentativas)

        enforcou = tentativas == 7
        acertou = "_" not in letras_acertadas
        print(letras_acertadas)

    if(acertou):
        victory()
    else:
        end_game(palavra_secreta)


